/******************************************************************************
【名称】IOポートレジスタ初期化
【再入】非リエントラント
【入力】なし
【出力】なし
【戻値】なし
【処理】I/Oポートレジスタの初期化
        使用するポートのレジスタを初期化します
******************************************************************************/
void IO_Init( void )
{
	// ポート6のビット4、ビット0を出力に指定します
	// (LED出力を行います)
	TZ.TOER.BIT.EA1 = 0x0;
	TZ.TOER.BIT.EA0 = 0x0;
	IO.PCR6 = 0x11;// 0ビット目、4ビット目(PCR60、PCR64を1(出力)に変更する)
	IO.PDR6.BIT.B0 = 0;
	IO.PDR6.BIT.B4 = 0;
}

/******************************************************************************
【名称】ポートデータレジスタ6操作処理
【再入】非リエントラント
【入力】ポートデータレジスタ6の設定値
【出力】なし
【戻値】なし
【処理】タイマー割り込み処理
******************************************************************************/
void IO_SetPDR6( unsigned char ucData )
{
	/* PDR6の値を引数に変更する */
	IO.PDR6.BYTE = ucData;
}
